#IGUALAR
r = 50 == 100 #false
print(r)
r = 100 == 100 #true
print(r)

#DISTINTO QUE
z = (50 != 10) #Como 50 no es igual que 100 pues debe ser false, pero lo niega, asi que es true al final
print(z)

#MENOR QUE
M = 100 < 100 # ya que 100 no es mejor que 100, false
print(M)
m = 100 <= 100 #es true ya que 100 es MENOR o IGUAL que 100
print(m)

#MAYOR QUE
P = 500 > 500 #Es false ya que 500 no es mayor que 500
print(P)
p = 500 >= 500 #Es true ya que 500 es MAYOR O IGUAL que 500
print(p)

